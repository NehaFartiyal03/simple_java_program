public class switchcase {
    public static void main(String[] args) {
      int item = 2;
        switch(item){
            case 1:
                System.out.println("prize is 100");
            break;
            case 2:
            System.out.println("prize is 200");
            break;
            case 3:
            System.out.println("prize is 400");
            break;
            default:
                System.out.println("invalid");

        }
    }
    
}
